void bdfCgenie::emit_bitmap(QTextStream& bdf, const QByteArray& buff)
{
    QByteArray bitmap((PIXEL_SIZE + 7) / 8, 0x00);

    bdf << QString("BITMAP\n");
    for (int y = 0; y < PIXEL_SIZE; y++) {
	const int py = y * 8 / PIXEL_SIZE;
	bitmap.fill(0x00);
	for (int x = 0; x < PIXEL_SIZE; x++) {
	    const int px = x * 8 / PIXEL_SIZE;
	    if (bit_chk(buff, px, py))
		bit_set(bitmap, x);
	}
	bdf << bitmap.toHex().toUpper();
	bdf << QString("\n");
    }
}

bool bdfCgenie::generate(const QString& fntname, const QString& bdfname)
{
    QFile fntfile(fntname);
    QFile bdffile(bdfname);
    QFile bmpfile(QStringLiteral("cgenie1.txt"));

    if (!fntfile.open(QIODevice::ReadOnly)) {
	return false;
    }

    if (!bdffile.open(QIODevice::WriteOnly)) {
	return false;
    }

    if (!bmpfile.open(QIODevice::WriteOnly)) {
	return false;
    }

    QTextStream bdf(&bdffile);
    bdf.setCodec("ISO8859-1");

    QTextStream bmp(&bmpfile);
    bmp.setCodec("UTF-8");

    bdf << QString("STARTFONT 2.1\n");
    QStringList font;
    font += FOUNDRY;
    font += FAMILY_NAME;
    font += WEIGHT_NAME;
    font += SLANT;
    font += SETWIDTH_NAME;
    font += ADD_STYLE_NAME;
    font += QString::number(PIXEL_SIZE);
    font += QString::number(POINT_SIZE);
    font += QString::number(RESOLUTION_X);
    font += QString::number(RESOLUTION_Y);
    font += SPACING;
    font += QString::number(AVERAGE_WIDTH);
    font += CHARSET_REGISTRY;
    font += CHARSET_ENCODING;

    bdf << QString("FONT -%1\n")
	      .arg(font.join(QChar('-')));

    bdf << QString("SIZE %1 %2 %3\n")
	      .arg(PIXEL_SIZE)
	      .arg(RESOLUTION_X)
	      .arg(RESOLUTION_Y);

    bdf << QString("FONTBOUNDINGBOX %1 %2 %3 %4\n")
	    .arg(BDF_BBOX_W)
	    .arg(BDF_BBOX_H)
	    .arg(BDF_BBOX_X)
	    .arg(BDF_BBOX_Y);

    if (m_properties.count() > 0) {
	bdf << QString("STARTPROPERTIES %1\n")
	       .arg(m_properties.count());
	foreach(const QString& property, m_properties) {
	    bdf << QString("%1\n")
		   .arg(property);
	}
	bdf << QString("ENDPROPERTIES\n");
    }

    bdf << QString("CHARS 256\n");

    for (int code = 0; code < 256; code++) {
	QByteArray buff;
	buff = fntfile.read(8);
	if (buff.length() != 8) {
	    qDebug("%s: failed to read font", __func__);
	    return false;
	}
	bmp << QString("/**** %1 (0x%2) ****/\n")
	       .arg(code, -3)
	       .arg(code, 2, 16, QChar('0'));
	for (int y = 0; y < 8; y++) {
	    const uchar bits = *reinterpret_cast<const uchar *>(buff.constData() + y);
	    bmp << QString("/* %1%2%3%4%5%6%7%8 */\n")
		   .arg(bits & 0x80 ? "[]" : "··")
		   .arg(bits & 0x40 ? "[]" : "··")
		   .arg(bits & 0x20 ? "[]" : "··")
		   .arg(bits & 0x10 ? "[]" : "··")
		   .arg(bits & 0x08 ? "[]" : "··")
		   .arg(bits & 0x04 ? "[]" : "··")
		   .arg(bits & 0x02 ? "[]" : "··")
		   .arg(bits & 0x01 ? "[]" : "··");
	}
	bmp << QString("/********************/\n");
	bdfGlyph glyph = m_glyphs.value(code);
	if (glyph.isNull()) {
	    bmp << QString("insert(new bdfGlyphObj(0x%1,0x%2,\"%3\"));\n\n")
		   .arg(code, 2, 16, QChar('0'))
		   .arg(code, 4, 16, QChar('0'))
		   .arg(QString("U+%1").arg(code, 4, 16, QChar('0')));
	} else {
	    bmp << QString("insert(new bdfGlyphObj(0x%1,0x%2,\"%3\"));\n\n")
	       .arg(glyph->ch(), 2, 16, QChar('0'))
	       .arg(glyph->uc(), 4, 16, QChar('0'))
	       .arg(glyph->name());
	}

	if (glyph.isNull()) {
	    // Undefined glyph
	    bdf << QString("STARTCHAR \"U+%1\"\n").arg(0xe000 + code, 4, 16, QChar('0'));
	    bdf << QString("ENCODING %1\n").arg(0xe000 + code);
	} else {
	    // Known glyph
	    bdf << QString("STARTCHAR \"%1\"\n").arg(glyph->name());
	    bdf << QString("ENCODING %1\n").arg(glyph->uc());
	}

	// Scaled width
	bdf << QString("SWIDTH %1 %2\n")
		.arg(SCALABLE_WIDTH)
		.arg(SCALABLE_HEIGHT);

	// Device width
	bdf << QString("DWIDTH %1 %2\n")
		.arg(DEVICE_WIDTH)
		.arg(DEVICE_HEIGHT);

	// Bounding box
	bdf << QString("BBX %1 %2 %3 %4\n")
		.arg(BDF_BBOX_W)
		.arg(BDF_BBOX_H)
		.arg(BDF_BBOX_X)
		.arg(BDF_BBOX_Y);

	emit_bitmap(bdf, buff);
	bdf << QString("ENDCHAR\n");
    }
    bdffile.close();
    fntfile.close();

    return true;
}

